    public loainghiphep getform(){
    loainghiphep a = new loainghiphep();
    a.setMaloai(txt_maloai.getText());
    a.setTenloai(txt_tenloai.getText());
    a.setHuongLuong(rd_tinhluong.isSelected());
    return a;
    
    }
    
    public void setform(loainghiphep b){
    txt_maloai.setText(String.valueOf(b.getMaloai()));
    txt_tenloai.setText(b.getTenloai());
        if (b.getHuongLuong()) {
            rd_tinhluong.setSelected(true);
            rd_tinhkoluong.setSelected(false);
        }else{
        rd_tinhluong.setSelected(false);
            rd_tinhkoluong.setSelected(true);
        
        }
    
    }

    @Override
    public void them() {
        loainghiphep entity = this.getform();
        dao.create(entity);
        this.fillTable();
    }

  @Override
public void sua() {
    loainghiphep entity = this.getform();
    if (entity == null) {
        XDialog.alert( "❌ Không thể cập nhật vì dữ liệu không hợp lệ.");
        return; // Dừng lại nếu null
    }

    dao.update(entity);
    this.fillTable();
    XDialog.alert( "✅ Cập nhật thành công");
}


    @Override
    public void xoa() {
        String id = txt_maloai.getText();
        dao.deleteById(Integer.valueOf(id));
        this.fillTable();
    }

      @Override
    public void edit() {
        int index = tb1.getSelectedRow();
        loainghiphep entity = list.get(index);
        this.setform(entity);

    }
    
    
    public void fillTable() {
       
         list = dao.findAll();
        DefaultTableModel model = (DefaultTableModel) tb1.getModel();
        model.setRowCount(0);
        for ( loainghiphep n : list) {
            model.addRow(new Object[]{
                n.getMaloai(),
                n.getTenloai(),
                n.getHuongLuong()? "Có" : "Không"
            });
        }
    }
