    public void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tb2.getModel();
        model.setRowCount(0); // Xóa dữ liệu cũ

        try {
            // Dùng biến maNV đã truyền từ constructor
            LocalDate now = LocalDate.now();
            int thang = now.getMonthValue();
            int nam = now.getYear();
              System.out.println("maNV = " + maNV + ", thang = " + thang + ", nam = " + nam);
            
            Thongkechamcong tk = dao.thongkechamcong(maNV, thang, nam);
            
            if (tk != null) {
                model.addRow(new Object[]{
                    tk.getDiMuon(),
                    tk.getVeSom(),
                    tk.getNghiLe(),
                    tk.getNghiPhep(),
                    tk.getNghiKhongPhep()
                });
            } else {
               
            }
        } catch (Exception e) {
            e.printStackTrace();
            XDialog.alert("Lỗi khi thống kê: " + e.getMessage());
        }
      

    }
    
    public chamcong getform() {
    chamcong a = new chamcong();

    String strMaNV = txt_manv.getText().trim();
    if (strMaNV.isEmpty()) {
        XDialog.alert("Mã nhân viên không được để trống!");
        return null;
    }

    try {
       String ma = strMaNV;
        a.setMaNV(ma);
    } catch (NumberFormatException e) {
        e.printStackTrace();
        XDialog.alert("Mã nhân viên không hợp lệ!");
        return null;
    }

    a.setHoten(txt_hoten.getText());

    try {
        SimpleDateFormat sdf = new SimpleDateFormat("HH:mm");
        Time checkin = new Time(sdf.parse(txt_checkin.getText()).getTime());
        Time checkout = new Time(sdf.parse(txt_checkout.getText()).getTime());
        a.setCheckIn(checkin);
        a.setCheckOut(checkout);
    } catch (Exception e) {
        e.printStackTrace();
        XDialog.alert("Check-in / Check-out sai định dạng! (HH:mm)");
        return null;
    }

    a.setDiLam(rd_co.isSelected());

    // Ngày làm việc
    try {
        String strNgay = txt_ngay.getText().trim();
        if (strNgay.isEmpty()) {
            XDialog.alert("Ngày làm việc không được để trống!");
            return null;
        }

        java.sql.Date ngay = java.sql.Date.valueOf(strNgay); // yêu cầu yyyy-MM-dd
        a.setNgay(ngay);
    } catch (Exception e) {
        e.printStackTrace();
        XDialog.alert("Ngày làm việc sai định dạng! (yyyy-MM-dd)");
        return null;
    }

    a.setGhiChu(txt_ghichu.getText());

    return a;
}

    
