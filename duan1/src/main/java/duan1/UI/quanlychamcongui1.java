   @Override
public void sua() {
    chamcong entity = this.getform(); // Lấy dữ liệu từ form

    if (entity == null) {
        XDialog.alert("Dữ liệu nhập không hợp lệ!");
        return;
    }

    try {
        // Hỏi xác nhận trước khi cập nhật
        int confirm = JOptionPane.showConfirmDialog(
                this, 
                "Bạn có chắc chắn muốn cập nhật bản ghi chấm công này?", 
                "Xác nhận cập nhật", 
                JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) return;

        dao.update(entity);           // Cập nhật vào CSDL
        XDialog.alert("Cập nhật thành công!");
        this.fillTable();            // Load lại dữ liệu bảng
                        // Nếu có phương thức clear
    } catch (Exception e) {
        e.printStackTrace();
        XDialog.alert("Cập nhật thất bại: " + e.getMessage());
    }
}


    
  @Override
public void xoa() {
    String id = txt_maCC.getText().trim();

    // Kiểm tra rỗng
    if (id.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn bản ghi cần xóa.", "Thông báo", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        int maCC = Integer.parseInt(id);

        // Xác nhận trước khi xóa
        int confirm = JOptionPane.showConfirmDialog(this, 
                "Bạn có chắc chắn muốn xóa mã chấm công " + maCC + "?", 
                "Xác nhận xóa", 
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            dao.deleteById(maCC);  // Xóa trong DAO
            JOptionPane.showMessageDialog(this, "Xóa thành công!");
            this.fillTable();      // Refresh bảng
                // Nếu có method clear
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Mã chấm công không hợp lệ!", "Lỗi", JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Lỗi khi xóa: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
    }
}

    
