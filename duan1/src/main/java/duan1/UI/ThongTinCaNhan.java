    public void setForm() {
    if (thongTin != null) {
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        txtmanv.setText(String.valueOf(thongTin.getMaNV()));
        txtten.setText(thongTin.getHoTen() != null ? thongTin.getHoTen() : "");
        txtemail.setText(thongTin.getEmail() != null ? thongTin.getEmail() : "");
        txtsdt.setText(thongTin.getSDT() != null ? thongTin.getSDT() : ""); // ✅ Thêm SDT tại đây
        txtgioitinh.setText(thongTin.getGioiTinh() != null ? thongTin.getGioiTinh() : "");
        txtngaysinh.setText(thongTin.getNgaySinh() != null ? sdf.format(thongTin.getNgaySinh()) : "");
        txtdiachi.setText(thongTin.getDiaChi() != null ? thongTin.getDiaChi() : "");
        txtmaphong.setText(String.valueOf(thongTin.getMaPhong()));
        txtchucvu.setText(String.valueOf(thongTin.getMaChucVu()));
        txtcccd.setText(thongTin.getCCCD() != null ? thongTin.getCCCD() : "");
        txttrinhdo.setText(thongTin.getTrinhDo() != null ? thongTin.getTrinhDo() : "");
        txtchuyennghanh.setText(thongTin.getChuyenNganh() != null ? thongTin.getChuyenNganh() : "");
        
        
        
        
          if (thongTin.getPhoto() != null && !thongTin.getPhoto().isEmpty()) {
            lb_anh.setIcon(new ImageIcon(thongTin.getPhoto()));
            lb_anh.setText(thongTin.getPhoto());
        } else {
            lb_anh.setIcon(null);
            lb_anh.setText("Chưa chọn ảnh");
        }
   
        txtngaytao.setText(thongTin.getNgayTao() != null ? sdf.format(thongTin.getNgayTao()) : "");
        txtnguoitao.setText(thongTin.getNguoiTao() != null ? thongTin.getNguoiTao() : "");
        txtngayvao.setText(thongTin.getNgayVao() != null ? sdf.format(thongTin.getNgayVao()) : "");
        rbotrangthai.setText(thongTin.getTrangThai() != null ? thongTin.getTrangThai() : "");
        
        
    } else {
        txtmanv.setText("");
        txtten.setText("");
        txtemail.setText("");
        txtsdt.setText(""); // ✅ Đặt trống SDT nếu null
        txtgioitinh.setText("");
        txtngaysinh.setText("");
        txtdiachi.setText("");
        txtmaphong.setText("");
        txtchucvu.setText("");
        txtcccd.setText("");
        txttrinhdo.setText("");
        txtchuyennghanh.setText("");
       
        txtngaytao.setText("");
        txtnguoitao.setText("");
        txtngayvao.setText("");
        rbotrangthai.setText("");
    }
}


