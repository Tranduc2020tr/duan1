        JFrame parentFrame = new JFrame(); // tạo frame cha
       String maNV = XAuth.user.getMaNV();// mã nhân viên cần lấy dữ liệu
        ThongTinCaNhan dialog = new ThongTinCaNhan(parentFrame, true);
        
        // Lấy dữ liệu nhân viên
