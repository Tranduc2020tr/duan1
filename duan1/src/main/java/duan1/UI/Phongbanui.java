    public phongban getform() {
        phongban a = new phongban();

        a.setTenpb(txt_tenpb.getText());
        a.setMota(txt_mota.getText());
        return a;
    }

    public void setform(phongban b) {
        txt_mapb.setText(String.valueOf(b.getMapb()));
        txt_tenpb.setText(b.getTenpb());
        txt_mota.setText(b.getMota());

    }

    public void filltbale() {
        nv = dao.findAll();
        defaultTableModel.setRowCount(0);
        for (phongban list : nv) {
            defaultTableModel.addRow(new Object[]{
                list.getMapb(),
                list.getTenpb(),
                list.getMota(),});
        }
    }

    @Override
    public void them() {
        phongban entity = this.getform();
        dao.create(entity);
        this.filltbale();
    }

    @Override
    public void sua() {
        phongban entity = this.getform();
        dao.update(entity);
        this.filltbale();
    }

    @Override
    public void xoa() {
        String id = txt_mapb.getText();
        dao.deleteById(Integer.valueOf(id));
        this.filltbale();
    }

    @Override
    public void edit() {
        int index = tb1.getSelectedRow();
        phongban entity = nv.get(index);
        this.setform(entity);

    }

    public void timkiem() {
        defaultTableModel.setRowCount(0);
        String ten = txt_timkiem.getText().trim().toLowerCase();

        for (phongban list : nv) {
            if (list.getTenpb().toLowerCase().contains(ten)) {
                defaultTableModel.addRow(new Object[]{
                    list.getMapb(),
                    list.getTenpb(),
                    list.getMota(),});
            }
        }

    }
