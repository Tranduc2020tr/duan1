    public void filltbale() {
        nv = dao.findAll();
        defaultTableModel.setRowCount(0);

        for (chamcong list : nv) {
            defaultTableModel.addRow(new Object[]{
                list.getMaChamCong(),
                list.getMaNV(),
                list.getHoten(),
                list.getCheckIn(),
                list.getCheckOut(),
                list.getTongGio(),
                list.isDiLam() ? "Có" : "Không",
                list.getNgay(),
                list.getGhiChu()
            });
        }
    }

   public void timkiem() {
    if (timer != null) timer.stop(); // ✅ tránh NullPointerException

    String ten = txt_timkiem.getText().trim().toLowerCase();
    defaultTableModel.setRowCount(0);

    for (chamcong list : nv) {
        if (list.getHoten().toLowerCase().contains(ten)) {
            defaultTableModel.addRow(new Object[]{
                list.getMaChamCong(),
                list.getMaNV(),
                list.getHoten(),
                list.getCheckIn(),
                list.getCheckOut(),
                list.getTongGio(),
                list.isDiLam() ? "Có" : "Không",
                list.getNgay(),
                list.getGhiChu()
            });
        }
    }

    if (timer != null) timer.start(); // ✅ bật lại nếu timer tồn tại
}


