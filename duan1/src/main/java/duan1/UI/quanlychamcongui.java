    private void tb1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb1MouseClicked
        // TODO add your handling code here:
        int row = tb1.getSelectedRow();
        if (row != -1) {
            Object objMaCC = tb1.getValueAt(row, 0);
            Object objMaNV = tb1.getValueAt(row, 1);
            Object objHoTen = tb1.getValueAt(row, 2);
            Object objCheckin = tb1.getValueAt(row, 3);
            Object objCheckout = tb1.getValueAt(row, 4);
            Object objTongGio = tb1.getValueAt(row, 5);
            Object objDiLam = tb1.getValueAt(row, 6);
            Object objNgay = tb1.getValueAt(row, 7);
            Object objGhiChu = tb1.getValueAt(row, 8);

            String maCC = objMaCC != null ? objMaCC.toString() : "";
            String maNV = objMaNV != null ? objMaNV.toString() : "";
            String hoTen = objHoTen != null ? objHoTen.toString() : "";
            String checkin = objCheckin != null ? objCheckin.toString() : "";
            String checkout = objCheckout != null ? objCheckout.toString() : "";
            String tongGio = objTongGio != null ? objTongGio.toString() : "";
            String diLam = objDiLam != null ? objDiLam.toString() : "";
            String ngay = objNgay != null ? objNgay.toString() : "";
            String ghiChu = objGhiChu != null ? objGhiChu.toString() : "";

            // Lấy form cha (đang là JDialog hoặc JPanel nằm trong JDialog)
            Window parentWindow = SwingUtilities.getWindowAncestor(this);
            if (parentWindow != null) {
                parentWindow.setVisible(false); // Ẩn form cha
            }

            // Mở form con (form3)
            quanlychamcongui1 dialog = new quanlychamcongui1(
                    parentWindow, // truyền form cha để làm owner
                    true, // modal
                    maCC, maNV, hoTen, checkin, checkout, tongGio, diLam, ngay, ghiChu
            );
            dialog.setLocationRelativeTo(null); // căn giữa màn hình (tuỳ chọn)
            dialog.setVisible(true); // show form con, sẽ block tại đây nếu modal

            // Sau khi form con đóng, hiện lại form cha (nếu cần)
            if (parentWindow != null) {
                parentWindow.setVisible(true);
            }
        }

    }//GEN-LAST:event_tb1MouseClicked

