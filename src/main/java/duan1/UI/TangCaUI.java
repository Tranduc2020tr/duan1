    private boolean validateTangCaForm(boolean isEdit) {
        String maNV = txt_manv.getText() != null ? txt_manv.getText().trim() : "";
        String maTangCa = txt_matangca.getText() != null ? txt_matangca.getText().trim() : "";
        String ngay = txt_ngay.getText() != null ? txt_ngay.getText().trim() : "";
        String loai = (String) cbx_loaitangca.getSelectedItem();
        boolean trangThaiChon = rd_duyet.isSelected() || rd_khongduyet.isSelected();

        if (isEdit && (maTangCa.isEmpty())) {
            XDialog.alert("❌ Vui lòng chọn bản ghi cần sửa (Mã tăng ca rỗng)!");
            return false;
        }
        if (maNV.isEmpty()) {
            XDialog.alert("❌ Vui lòng nhập Mã NV!");
            txt_manv.requestFocus();
            return false;
        }
        if (ngay.isEmpty()) {
            XDialog.alert("❌ Vui lòng chọn Ngày!");
            txt_ngay.requestFocus();
            return false;
        }
        // Không cho phép ngày lớn hơn hôm nay
        LocalDate today = LocalDate.now();
        LocalDate inputDate = parseToLocalDate(ngay);
        if (inputDate == null) {
            XDialog.alert("❌ Định dạng ngày không hợp lệ! Hãy dùng dd/MM/yyyy, dd-MM-yyyy hoặc yyyy-MM-dd");
            txt_ngay.requestFocus();
            return false;
        }
        if (inputDate.isAfter(today)) {
            XDialog.alert("❌ Không được chọn ngày lớn hơn hôm nay!");
            txt_ngay.requestFocus();
            return false;
        }
        if (loai == null || loai.trim().isEmpty()) {
            XDialog.alert("❌ Vui lòng chọn Loại tăng ca!");
            cbx_loaitangca.requestFocus();
            return false;
        }
        if (!trangThaiChon) {
            XDialog.alert("❌ Vui lòng chọn Trạng thái!");
            return false;
        }
        return true;
    }

    private LocalDate parseToLocalDate(String input) {
        if (input == null) return null;
        String s = input.trim();
        if (s.isEmpty()) return null;
        DateTimeFormatter[] fmts = new DateTimeFormatter[] {
            DateTimeFormatter.ofPattern("yyyy-MM-dd"),
            DateTimeFormatter.ofPattern("dd/MM/yyyy"),
            DateTimeFormatter.ofPattern("dd-MM-yyyy")
        };
        for (DateTimeFormatter f : fmts) {
            try {
                return LocalDate.parse(s, f);
            } catch (DateTimeParseException ignore) {}
        }
        return null;
    }

