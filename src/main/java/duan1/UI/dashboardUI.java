    private void setUserInfo() {
    if (XAuth.user != null) {
        lb_name.setText(XAuth.user.getFullname()); // hoặc getName(), tuỳ theo entity User
        if(XAuth.user.isVaiTro()){
            lb_vaitro.setText("Quản lý");
        }else{
             lb_vaitro.setText("Nhân viên");
        }
    } else {
        lb_name.setText("Chưa đăng nhập");
        lb_vaitro.setText("Không xác định");
    }
}
    
    
