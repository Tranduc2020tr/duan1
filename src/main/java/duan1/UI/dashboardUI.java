    private void loadDashboard() {
    dashboardipml dao = new dashboardipml();
    List<dashboard> list = dao.findAll();
    
    if (!list.isEmpty()) {
        dashboard dash = list.get(0); // Vì view chỉ trả về 1 dòng
        
        lb_TongNhanVien.setText(String.valueOf(dash.getTongNhanVien()));
        lb_chamcong.setText(String.valueOf(dash.getTongChamCongHomNay()));
        lb_nghiphep.setText(String.valueOf(dash.getTongNghiPhepHomNay()));
    }
}

