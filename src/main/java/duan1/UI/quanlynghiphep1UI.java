public void fillTable() {
    DefaultTableModel model = (DefaultTableModel) tb1.getModel();
    model.setRowCount(0); // X√≥a d·ªØ li·ªáu c≈© trong b·∫£ng

    list = dao.findAll(); // L·∫•y to√†n b·ªô danh s√°ch ngh·ªâ ph√©p

    for (NghiPhep np : list) {
        // Format tr·∫°ng th√°i v·ªõi m√†u s·∫Øc
        String trangThai = np.getTrangthai();
        if (trangThai != null) {
            if (trangThai.equalsIgnoreCase("ƒê√£ duy·ªát")) {
                trangThai = "‚úÖ " + trangThai;
            } else if (trangThai.equalsIgnoreCase("T·ª´ ch·ªëi")) {
                trangThai = "‚ùå " + trangThai;
            } else if (trangThai.equalsIgnoreCase("Ch·ªù duy·ªát")) {
                trangThai = "‚è≥ " + trangThai;
            }
        }
        
        Object[] row = {
            np.getMayeucau(),
            np.getManv(),
            np.getTenloai(),
            np.getHoten(),
            np.getNgaybatdau(),
            np.getNgayketthuc(),
            np.getLydo(),
            trangThai,
            np.getNgaygui(),
            np.getNgayxuly(),
            np.getNguoixuly(),
            np.getGhichuxuly()
        };
        model.addRow(row);
    }
}

public NghiPhep getForm() {
    NghiPhep np = new NghiPhep();

    try {
        np.setMayeucau(txt_mayeucau.getText());
    } catch (NumberFormatException e) {
        XDialog.alert("‚ùå M√£ y√™u c·∫ßu kh√¥ng h·ª£p l·ªá!");
        return null;
    }

    np.setLydo(txt_lydo.getText().trim());
    np.setTrangthai(rd_duyet.isSelected() ? "ƒê√£ duy·ªát" : "T·ª´ ch·ªëi");

    String now = LocalDateTime.now().format(DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss"));
    np.setNgayxuly(now);
    np.setNguoixuly(XAuth.user.getFullname());
    np.setGhichuxuly(txt_ghichuxuly.getText().trim());
    return np;
}

