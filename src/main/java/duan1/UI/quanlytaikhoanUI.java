    public quanlytaikhoan getform() {
        quanlytaikhoan a = new quanlytaikhoan();
        a.setTendangnhap(txt_username.getText());
        a.setMatkhau(txt_password.getText());
        a.setManv(txt_manv.getText());
        a.setTrangthai(rd_hd.isSelected());
        int selectedIndex = cbx_phanquyen.getSelectedIndex();
        if (selectedIndex >= 0 && selectedIndex < roles.size()) {
            role selectedRole = roles.get(selectedIndex);
            a.setRoleid(selectedRole.getRoleid());
            a.setRolename(selectedRole.getRolename());
            boolean isAdmin = selectedRole.getRolename() != null && selectedRole.getRolename().equalsIgnoreCase("Admin");
            a.setVaitro(isAdmin);
        } else {
            a.setRoleid(0);
            a.setRolename(null);
            a.setVaitro(false);
        }
        return a;
    }

    public void setform(quanlytaikhoan b) {
        txt_manv.setText(String.valueOf(b.getManv()));
        txt_username.setText(b.getTendangnhap());
        txt_password.setText(b.getMatkhau());
        txt_hoten.setText(b.getHoten());
        
        // Set selected role in combo box
        int selIndex = -1;
        if (roles != null && !roles.isEmpty()) {
            for (int i = 0; i < roles.size(); i++) {
                if (b.getRoleid() != 0 && roles.get(i).getRoleid() == b.getRoleid()) {
                    selIndex = i;
                    break;
                }
            }
            if (selIndex == -1 && b.getRolename() != null) {
                for (int i = 0; i < roles.size(); i++) {
                    if (b.getRolename().equalsIgnoreCase(roles.get(i).getRolename())) {
                        selIndex = i;
                        break;
                    }
                }
            }
        }
        if (selIndex >= 0) {
            cbx_phanquyen.setSelectedIndex(selIndex);
        } else if (cbx_phanquyen.getItemCount() > 0) {
            cbx_phanquyen.setSelectedIndex(0);
        }

        // Trạng thái
        if (b.isTrangthai()) {
            rd_hd.setSelected(true);     
            rd_khd.setSelected(false);
        } else {
            rd_hd.setSelected(false);
            rd_khd.setSelected(true);    
        }
    }
    
    public void fillTable() {
    DefaultTableModel model = (DefaultTableModel) tb1.getModel();
    model.setRowCount(0); // Xóa trắng bảng

    nv = dao.findAll(); // <-- cập nhật lại danh sách toàn cục
    for (quanlytaikhoan tk : nv) {
        String vaiTro = tk.getRolename() != null ? tk.getRolename() : "";
        String trangThai = tk.isTrangthai() ? "Hoạt động" : "Khóa";

        model.addRow(new Object[]{
            tk.getManv(),
            tk.getHoten(),
            tk.getTendangnhap(),
            tk.getMatkhau(),
            vaiTro,
            trangThai
        });
    }
}
    
    
