    @Override
    public void sua() {
         if (!validateAccountForm()) {
            return;
        }
         quanlytaikhoan entity = this.getform();

        // Cảnh báo & giữ nguyên phân quyền nếu tài khoản hiện tại là Admin
        try {
            quanlytaikhoan current = dao.findById(entity.getTendangnhap());
            if (current != null && current.getRoleid() == 1 && entity.getRoleid() != current.getRoleid()) {
                JOptionPane.showMessageDialog(this, "Không thể sửa phân quyền nếu bạn là Admin.", "Thông báo", JOptionPane.WARNING_MESSAGE);
                // Đặt lại combobox về phân quyền hiện tại trong DB
                int selIndex = -1;
                for (int i = 0; i < roles.size(); i++) {
                    if (roles.get(i).getRoleid() == current.getRoleid()) {
                        selIndex = i;
                        break;
                    }
                }
                if (selIndex >= 0) {
                    cbx_phanquyen.setSelectedIndex(selIndex);
                    role r = roles.get(selIndex);
                    entity.setRoleid(r.getRoleid());
                    entity.setRolename(r.getRolename());
                }
            }
        } catch (Exception ex) {
            // Bỏ qua lỗi đọc DB, tiếp tục update theo logic DAO (DAO đã chặn đổi role Admin)
        }

        dao.update(entity);
         this.fillTable();
    }

    @Override
    public void eidt() {
       int index = tb1.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để chỉnh sửa!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Reload data for table 1 to ensure correct data
        List<quanlytaikhoan> table1Data = dao.findAll();
        if (index >= table1Data.size()) {
            JOptionPane.showMessageDialog(this, "Dữ liệu không hợp lệ!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        quanlytaikhoan entity = table1Data.get(index);
        this.setform(entity);
    }
    
