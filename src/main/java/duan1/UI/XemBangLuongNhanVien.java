    private void fillTableTheoNV() {
        String manv = XAuth.user.getMaNV(); // Lấy mã nhân viên đăng nhập
        DefaultTableModel model = (DefaultTableModel) jtb_bang.getModel();
        model.setRowCount(0);

        List<Luong> list = controller.layTatCaBangLuong(manv);

        for (Luong l : list) {
            Object[] row = new Object[]{
                l.getMaluong(),
                l.getManv(),
                l.getThang(),
                l.getNam(),
                formatVND(l.getLuongcoban()),
                formatVND(l.getLuongphucap()),
                formatVND(l.getThuong()),
                formatVND(l.getKhautru()),
                formatVND(l.getTongluong()),
                l.getNgayTao(),
                
            };
            model.addRow(row);
        }
    }

    public void timkiem() {
        try {
            // Lấy mã nhân viên từ người dùng đang đăng nhập
            String manv = XAuth.user.getMaNV();

            // Lấy tháng và năm từ ô nhập
            int thang = Integer.parseInt(txt_thang.getText().trim());
            int nam = Integer.parseInt(txt_nam.getText().trim());

            // Gọi controller để lấy dữ liệu lương sau khi tìm kiếm
            List<Luong> list = controller.searchLuong(manv, thang, nam);
            DefaultTableModel model = (DefaultTableModel) jtb_bang.getModel();
            model.setRowCount(0); // Xóa bảng cũ

            for (Luong l : list) {
                Object[] row = new Object[]{
                    l.getMaluong(),
                    l.getManv(),
                    l.getThang(),
                    l.getNam(),
                    formatVND(l.getLuongcoban()),
                    formatVND(l.getLuongphucap()),
                    formatVND(l.getThuong()),
                    formatVND(l.getKhautru()),
                    formatVND(l.getTongluong()),
                    l.getNgayTao(),
                    l.getNguoiTao(),
                    l.getGhiChu()
                };
                model.addRow(row);
            }

            if (list.isEmpty()) {
                XDialog.alert("Không tìm thấy bảng lương trong tháng/năm đã nhập.");
            }

        } catch (NumberFormatException e) {
            XDialog.alert("Vui lòng nhập đúng định dạng số cho Tháng và Năm");
            fillTableTheoNV(); 
        } catch (Exception e) {
            XDialog.alert("lỗi" + e.getMessage());
            e.printStackTrace();
        }
    }

    private String formatVND(BigDecimal amount) {
        if (amount == null) {
            return "";
        }
        BigDecimal truncated = amount.setScale(0, RoundingMode.DOWN);
        NumberFormat vnCurrency = NumberFormat.getCurrencyInstance(Locale.forLanguageTag("vi-VN"));
        vnCurrency.setMaximumFractionDigits(0);
        vnCurrency.setMinimumFractionDigits(0);
        return vnCurrency.format(truncated);
    }

