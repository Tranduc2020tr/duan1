    public void edit(){
     int selectedRow = tb1.getSelectedRow();

        if (selectedRow >= 0) {
            txt_male.setText(tb1.getValueAt(selectedRow, 0).toString());
            txt_ngay.setText(tb1.getValueAt(selectedRow, 1).toString());
            txt_tenle.setText(tb1.getValueAt(selectedRow, 2).toString());

            String coTinhLuong = tb1.getValueAt(selectedRow, 3).toString();

            if (coTinhLuong.equals("C√≥ l∆∞∆°ng")) {
                rd_tinhluong.setSelected(true);
            } else {
                rd_tinhkoluong.setSelected(true);
            }
        }
    }
