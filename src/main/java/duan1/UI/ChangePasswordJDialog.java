   @Override
public void save() {
  String email = txt_email.getText().trim().toLowerCase();
    String maNhap = txt_maxacnhan.getText().trim();
    String matKhauMoi = txtPassword.getText();
    String xacNhanMK = txtConfirm.getText();

    System.out.println("Email nhập vào: [" + email + "]");
    System.out.println("Mã xác nhận nhập vào: [" + maNhap + "]");
    System.out.println("Mã xác nhận hệ thống gửi: [" + maXacNhanDaGui + "]");

    // Kiểm tra rỗng
    if (email.isEmpty() || matKhauMoi.isEmpty() || xacNhanMK.isEmpty() || maNhap.isEmpty()) {
        XDialog.alert("Vui lòng điền đầy đủ thông tin!");
        return;
    }

    // So sánh mã xác nhận
    if (!maNhap.equals(maXacNhanDaGui)) {
        XDialog.alert("Mã xác nhận không chính xác!");
        return;
    }

    // Kiểm tra xác nhận mật khẩu
    if (!matKhauMoi.equals(xacNhanMK)) {
        XDialog.alert("Mật khẩu xác nhận không khớp!");
        return;
    }

    // Cập nhật mật khẩu trong database
   boolean doiThanhCong = nhanvienimpl.doiMatKhauTheoEmail(email, matKhauMoi);

    if (doiThanhCong) {
        XDialog.alert("Đổi mật khẩu thành công!");
        this.dispose(); // hoặc xóa trắng các ô nhập nếu cần
    } else {
        XDialog.alert("Không tìm thấy email hoặc lỗi hệ thống.");
    }
}


