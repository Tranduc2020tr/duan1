    private void configureUiForEntity(dangkitangca entity) {
        if (entity == null) {
            configureUiOnlyCheckin();
            return;
        }
        String trangThai = entity.getTrangThai();
        boolean daDuyet = trangThai != null && trangThai.equalsIgnoreCase("Đã duyệt");
        if (daDuyet) {
            configureUiOnlyCheckout();
        } else {
            configureUiOnlyDelete();
        }
    }

    public void openForAddMode() {
        this.addMode = true;
        configureUiForAddMode();
    }

    public dangkitangca getform() {
        dangkitangca a = new dangkitangca();
        String manv = XAuth.user.getMaNV();
        a.setMaNV(manv);
        String loaiTangCa = cbx_Loaitangca.getSelectedItem().toString(); // Lấy loại
        double heSo = 0;
        String loaiTangCaDB = "";
        switch (loaiTangCa) {
            case "Ngày Thường":
                heSo = 1.5;
                loaiTangCaDB = "NgayThuong";
                break;
            case "Thứ 7, CN":
                heSo = 2.0;
                loaiTangCaDB = "T7_CN";
                break;
            case "Ngày Lễ":
                heSo = 3.0;
                loaiTangCaDB = "Le";
                break;
            default:

                break;
        }

// Bây giờ bạn có:
        a.setHeSo(heSo);
        a.setLoaiTangCa(loaiTangCaDB);

        String ngay = LocalDate.now().toString(); // "2025-07-30"
        Date sqlDate = Date.valueOf(ngay);
        a.setNgay(String.valueOf(sqlDate));

        LocalTime gio = LocalTime.now();
        String gioDinhDang = gio.toString().substring(0, 8); // "23:10:20"
        a.setCheckin(gioDinhDang);

        a.setGhiChu(txt_ghichu.getText());
        String ten = XAuth.user.getFullname();
        a.setNguoiTao(ten);
        return a;
    }

