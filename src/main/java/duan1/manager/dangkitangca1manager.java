    @Override
    public void xoa() {
        try {
            String rawId = txt_matangca.getText();
            String idToDelete = null;

            if (rawId != null && !rawId.trim().isEmpty()) {
                idToDelete = rawId.trim();
            }

            if (idToDelete == null) {
                // Lấy bản ghi hôm nay của NV hiện tại
                String maNV = XAuth.user.getMaNV();
                String ngayHienTai = java.time.LocalDate.now().toString();
                dangkitangca record = dao.findAnyOvertimeRecord(maNV, ngayHienTai);
                if (record == null) {
                    XDialog.alert("❌ Không có đăng ký tăng ca để xoá!");
                    return;
                }
                if ("Đã duyệt".equalsIgnoreCase(record.getTrangThai())) {
                    XDialog.alert("❌ Ca đã được duyệt, không thể xoá!");
                    return;
                }
                if (record.getMaTangCa() == null || record.getMaTangCa().trim().isEmpty()) {
                    XDialog.alert("❌ Mã tăng ca không hợp lệ!");
                    return;
                }
                idToDelete = record.getMaTangCa().trim();
            }

            dao.deleteById(idToDelete);
            XDialog.alert("✅ Xoá đăng ký tăng ca thành công");
            txt_matangca.setText("");
            refreshUiStateBasedOnApproval();
        } catch (Exception ex) {
            ex.printStackTrace();
            XDialog.alert("❌ Lỗi khi xoá đăng ký tăng ca!");
        }
    }

