    private void refreshUiStateBasedOnApproval() {
        try {
            // Nếu đang ở chế độ thêm mới
            if (addMode) {
                configureUiForAddMode();
                return;
            }

            duan1.entity.dangkitangca record = null;

            // Ưu tiên tìm theo mã trên form
            String rawId = txt_matangca.getText();
            if (rawId != null && !rawId.trim().isEmpty()) {
                record = dao.findById(rawId.trim()); // Không tách số
            }

            // Nếu không tìm thấy => tìm bản ghi hôm nay của nhân viên hiện tại
            if (record == null) {
                String maNV = XAuth.user.getMaNV();
                String ngayHienTai = java.time.LocalDate.now().toString();
                record = dao.findAnyOvertimeRecord(maNV, ngayHienTai);
            }

            // Nếu vẫn không có => chỉ cho checkin
            if (record == null) {
                configureUiOnlyCheckin();
                return;
            }

            // Kiểm tra trạng thái duyệt
            boolean daDuyet = "Đã duyệt".equalsIgnoreCase(record.getTrangThai());

            if (daDuyet) {
                configureUiOnlyCheckout();
            } else {
                configureUiOnlyDelete();
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

