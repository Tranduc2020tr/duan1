    @Override
    public void checkin() {
        try {
            // Kiểm tra xem đã checkin hôm nay chưa
            String maNV = XAuth.user.getMaNV();
            String ngayHienTai = LocalDate.now().toString();
            dangkitangca existingRecord = dao.findAnyOvertimeRecord(maNV, ngayHienTai);

            if (existingRecord != null) {
                XDialog.alert("❌ Bạn đã có ca tăng ca hôm nay rồi!");
                return;
            }

            dangkitangca entity = getform();
            if (entity == null) {
                return;
            }

            dao.create(entity);
            XDialog.alert("✅ Checkin tăng ca thành công!");
            refreshUiStateBasedOnApproval();

        } catch (Exception ex) {
            ex.printStackTrace();
            XDialog.alert("❌ Lỗi khi checkin tăng ca!");
        }
    }

    @Override
    public void checkout() {
        try {
            String maNV = XAuth.user.getMaNV();
            LocalTime gio = LocalTime.now();
            String gioDinhDang = gio.format(DateTimeFormatter.ofPattern("HH:mm:ss"));
            String ngayHienTai = LocalDate.now().toString();

            // Chặn: nếu hôm nay đã có bản ghi và đã checkout rồi, không cho checkout nữa
            dangkitangca anyToday = dao.findAnyOvertimeRecord(maNV, ngayHienTai);
            if (anyToday != null && anyToday.getCheckout() != null && !anyToday.getCheckout().trim().isEmpty()) {
                XDialog.alert("❌ Bạn đã check out tăng ca hôm nay rồi!");
                return;
            }

            // Ưu tiên dùng mã tăng ca trên form nếu có (khi mở từ bảng)
            String rawId = txt_matangca.getText();
            if (rawId != null && !rawId.trim().isEmpty()) {
                dao.updateCheckout(rawId.trim(), gioDinhDang);
                XDialog.alert("✅ Check out tăng ca thành công!");
                refreshUiStateBasedOnApproval();
                return;
            }

            // Nếu không có mã trên form, tìm record tăng ca hiện tại của nhân viên theo ngày hôm nay
            dangkitangca currentRecord = dao.findCurrentOvertimeRecord(maNV, ngayHienTai);
            if (currentRecord == null) {
                XDialog.alert("❌ Không tìm thấy ca tăng ca nào để check out!");
                return;
            }

            // Cập nhật checkout với mã tăng ca thực tế
            dao.updateCheckout(currentRecord.getMaTangCa(), gioDinhDang);

            XDialog.alert("✅ Check out tăng ca thành công!");
            refreshUiStateBasedOnApproval();
        } catch (Exception ex) {
            ex.printStackTrace();
            XDialog.alert("❌ Lỗi khi check out!");
        }
    }

    public void setForm(dangkitangca entity) {
        if (entity != null) {
            // Set các giá trị vào form để chỉnh sửa
            cbx_Loaitangca.setSelectedItem(entity.getLoaiTangCa());
            txt_ghichu.setText(entity.getGhiChu());
            txt_matangca.setText(String.valueOf(entity.getMaTangCa()));
            // Có thể thêm các field khác nếu cần
            this.addMode = false;
            configureUiForEntity(entity);
        }
    }

